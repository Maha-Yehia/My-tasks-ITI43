var par = document.getElementById("main");

function font(myFont) {
    par.style.fontFamily = myFont;
}
function toAlign(myAlign) {
    par.style.textAlign = myAlign;
}
function toheight(myHeight) {
    par.style.lineHeight = myHeight;
}
function spacing(mySpace) {
    par.style.letterSpacing = mySpace;
}
function indent(myIndent) {
    par.style.textIndent = myIndent;
}
function transform(myTransform) {
    par.style.textTransform = myTransform;
}
function decoration(myDecoration) {
    par.style.textDecoration = myDecoration;
}
function bStyle(style) {
    par.style.borderStyle = style;
}
function bClr(clr) {
    par.style.borderColor = clr;
}
